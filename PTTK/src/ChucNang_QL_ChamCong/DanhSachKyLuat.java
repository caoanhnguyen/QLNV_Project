    private void tbl_KyLuatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_KyLuatMouseClicked
        // TODO add your handling code here:
        int row = tbl_KyLuat.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) tbl_KyLuat.getModel();
        String tienThuong = dtm.getValueAt(row, 1).toString();
        tfSoTien_DanhSachKyLuat.setText(tienThuong);
        int soTien = Integer.parseInt(tfSoTien_DanhSachKyLuat.getText());
        int soLan = Integer.parseInt(tfSoLan_DánhachKyLuat.getText());
        int tienSanCo = soTien * soLan;
        tfTienSanCo_DanhSachKyLuat.setText(String.valueOf(tienSanCo));
    }//GEN-LAST:event_tbl_KyLuatMouseClicked

