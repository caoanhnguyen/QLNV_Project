    private void loadDataToTableKL(List<Model.kyLuat> kyLuatList){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã kỷ luật");
        dtm.addColumn("Tiền kỷ luật");
        dtm.addColumn("Lý do");
        
        Object[] row = new Object[3];
        for(Model.kyLuat kl : kyLuatList){
            row[0] = kl.getMaKyLuat();
            row[1] = kl.getTienKyLuat();
            row[2] = kl.getLyDo();
            dtm.addRow(row);
        }
        tbl_KyLuat.setModel(dtm);
    }
