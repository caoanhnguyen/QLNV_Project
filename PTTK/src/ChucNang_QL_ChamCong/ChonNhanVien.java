    private void loadDataToTable(List<NhanVien> ListNV){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã nhân viên");
        dtm.addColumn("Họ tên");
        dtm.addColumn("CCCD");
        dtm.addColumn("Phòng ban");
        Object[] row = new Object[4];
        for(Model.NhanVien nv : ListNV){
            row[0] = nv.getMaNV();
            row[1] = nv.getHoTen();
            row[2] = nv.getCMND_CCCD();
            row[3] = phongBanDAO.getTenPhongBanByMaNV(nv.getMaNV());
            dtm.addRow(row);
        }
        tbl_ChonNhanVien.setModel(dtm);
    }

