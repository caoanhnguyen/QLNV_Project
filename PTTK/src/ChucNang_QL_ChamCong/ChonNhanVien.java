    private void tbl_ChonNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ChonNhanVienMouseClicked
        // TODO add your handling code here:
        int row = tbl_ChonNhanVien.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) tbl_ChonNhanVien.getModel();
        String maNV = dtm.getValueAt(row, 0).toString();
        String hoTen = dtm.getValueAt(row, 1).toString();
        String CCCD = dtm.getValueAt(row, 2).toString();
        String phongBan = dtm.getValueAt(row, 3).toString();
        tfMaNhanVien_ChonNhanVien.setText(maNV);
        tfHoTen_ChonNhanVien.setText(hoTen);
        tfSoCCCD_ChonNhanVien.setText(CCCD);
        tfPhong_ChonNhanVien.setText(phongBan);
    }//GEN-LAST:event_tbl_ChonNhanVienMouseClicked

    private void bttHoanThanh_ChonNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttHoanThanh_ChonNhanVienActionPerformed
        // TODO add your handling code here:
        String maNV = tfMaNhanVien_ChonNhanVien.getText();
        Model.NhanVien nv = nhanVienDAO.getNVByMaNV(maNV);
        Model.Luong luong = luongDAO.getLuongByMaNV(maNV);
        if(maNV.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn nhân viên!");
        }
        else{
            CCform.txtMaPhong_CC.setText(nv.getMaPB());
            CCform.txtMaLuong_CC.setText(nv.getMaLuong());  
            CCform.txtLuongCB_CC.setText(String.valueOf(luong.getLuongCoBan()));
            CCform.txtPCCV_CC.setText(Integer.toString(luong.getPhuCapCV()));
            CCform.txtPCK_CC.setText(Integer.toString(luong.getPhuCapKhac()));
            CCform.cbMaNV_CC.setSelectedItem(maNV);
            this.dispose();
        }
        
    }//GEN-LAST:event_bttHoanThanh_ChonNhanVienActionPerformed

